import { $, semver } from "bun"
import path from "path"

const rootPkgPath = path.resolve(import.meta.dir, "../../../package.json")
const rootPkg = await Bun.file(rootPkgPath).json()
const expectedBunVersion = rootPkg.packageManager?.split("@")[1]

if (!expectedBunVersion) {
  throw new Error("packageManager field not found in root package.json")
}

// relax version requirement
const expectedBunVersionRange = `^${expectedBunVersion}`

if (!semver.satisfies(process.versions.bun, expectedBunVersionRange)) {
  throw new Error(`This script requires bun@${expectedBunVersionRange}, but you are using bun@${process.versions.bun}`)
}

const env = {
  NOVAHUB_CHANNEL: process.env["NOVAHUB_CHANNEL"],
  NOVAHUB_BUMP: process.env["NOVAHUB_BUMP"],
  NOVAHUB_VERSION: process.env["NOVAHUB_VERSION"],
  NOVAHUB_RELEASE: process.env["NOVAHUB_RELEASE"],
}
const CHANNEL = await (async () => {
  if (env.NOVAHUB_CHANNEL) return env.NOVAHUB_CHANNEL
  if (env.NOVAHUB_BUMP) return "latest"
  if (env.NOVAHUB_VERSION && !env.NOVAHUB_VERSION.startsWith("0.0.0-")) return "latest"
  return await $`git branch --show-current`.text().then((x) => x.trim())
})()
const IS_PREVIEW = CHANNEL !== "latest"

const VERSION = await (async () => {
  if (env.NOVAHUB_VERSION) return env.NOVAHUB_VERSION
  if (IS_PREVIEW) return `0.0.0-${CHANNEL}-${new Date().toISOString().slice(0, 16).replace(/[-:T]/g, "")}`
  const version = await fetch("https://registry.npmjs.org/novahub/latest")
    .then((res) => {
      if (!res.ok) throw new Error(res.statusText)
      return res.json()
    })
    .then((data: any) => data.version)
  const [major, minor, patch] = version.split(".").map((x: string) => Number(x) || 0)
  const t = env.NOVAHUB_BUMP?.toLowerCase()
  if (t === "major") return `${major + 1}.0.0`
  if (t === "minor") return `${major}.${minor + 1}.0`
  return `${major}.${minor}.${patch + 1}`
})()

export const Script = {
  get channel() {
    return CHANNEL
  },
  get version() {
    return VERSION
  },
  get preview() {
    return IS_PREVIEW
  },
  get release() {
    return env.NOVAHUB_RELEASE
  },
}
console.log(`novahub script`, JSON.stringify(Script, null, 2))
